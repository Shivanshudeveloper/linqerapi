const mongoose = require("mongoose");
// Schema
const usersDataSchema = new mongoose.Schema({
  fName: {
    type: String,
    required: true,
  },
  lName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  plan: {
    type: String,
    required: true,
  },
  amount: {
    type: Number,
    required: true,
  },
});
const users = mongoose.model("linqerUsers", usersDataSchema);
module.exports = users;
